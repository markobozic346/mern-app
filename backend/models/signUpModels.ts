import mongoose from "mongoose";

const signupTemplate = new mongoose.Schema({
  email: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const mongooseModel = mongoose.model("usertable", signupTemplate);

export default mongooseModel;
